# Collections Tuple Basics

professions = ("Front end", "Back end", "DevOps", "QA")

print("Professions in the industry: " + str(professions))
print(type(professions))

print("Print cell # 1: " + str(professions[1]))
print("Print cell in the last collection: " + str(professions[-1]))
print("Print range of cell in the collection: " + str(professions[1:3]))
print("Length of the collection: " + str(len(professions)))